static void fnv1aUpdate(uint64_t* hash, const void* data, size_t len) {
    if (!hash || !data) return;
    const unsigned char* bytes = (const unsigned char*)data;
    for (size_t i = 0; i < len; ++i) {
        *hash ^= bytes[i];
        *hash *= FNV1A64_PRIME;
    }
}

static void fnv1aUpdateUInt32(uint64_t* hash, uint32_t value) {
    fnv1aUpdate(hash, &value, sizeof(value));
}

static void fnv1aUpdateInt(uint64_t* hash, int value) {
    fnv1aUpdate(hash, &value, sizeof(value));
}

static void fnv1aUpdateUInt64(uint64_t* hash, uint64_t value) {
    fnv1aUpdate(hash, &value, sizeof(value));
}

static bool hasPathPrefix(const char* path, const char* prefix) {
    if (!path || !prefix) {
        return false;
    }
    size_t prefix_len = strlen(prefix);
    while (prefix_len > 0) {
#ifndef _WIN32
        if (prefix[prefix_len - 1] != '/') {
            break;
        }
#else
        if (prefix[prefix_len - 1] != '/' && prefix[prefix_len - 1] != '\\') {
            break;
        }
#endif
        prefix_len--;
    }
    if (prefix_len == 0) {
        return false;
    }
    if (strncmp(path, prefix, prefix_len) != 0) {
        return false;
    }
#ifndef _WIN32
    char next = path[prefix_len];
    return next == '/' || next == '\0';
#else
    char next = path[prefix_len];
    return next == '/' || next == '\\' || next == '\0';
#endif
}

static bool isLikelyTemporaryPath(const char* path) {
    if (!path || !path[0]) {
        return false;
    }

    const char* candidates[8];
    size_t candidate_count = 0;

    const char* env_tmp = getenv("TMPDIR");
    if (env_tmp && env_tmp[0]) {
        candidates[candidate_count++] = env_tmp;
    }
    env_tmp = getenv("TEMP");
    if (env_tmp && env_tmp[0]) {
        candidates[candidate_count++] = env_tmp;
    }
    env_tmp = getenv("TMP");
    if (env_tmp && env_tmp[0]) {
        bool duplicate = false;
        for (size_t i = 0; i < candidate_count; ++i) {
            if (strcmp(candidates[i], env_tmp) == 0) {
                duplicate = true;
                break;
            }
        }
        if (!duplicate) {
            candidates[candidate_count++] = env_tmp;
        }
    }

#ifndef _WIN32
    const char* defaults[] = { "/tmp", "/var/tmp", "/private/tmp", "/private/var/tmp", "/dev/shm" };
#else
    const char* defaults[] = { "C\\Windows\\Temp", "C:/Windows/Temp", "C\\Temp", "C:/Temp" };
#endif
    for (size_t i = 0; i < sizeof(defaults) / sizeof(defaults[0]); ++i) {
        bool duplicate = false;
        for (size_t j = 0; j < candidate_count; ++j) {
            if (strcmp(candidates[j], defaults[i]) == 0) {
                duplicate = true;
                break;
            }
        }
        if (!duplicate) {
            candidates[candidate_count++] = defaults[i];
        }
    }

    for (size_t i = 0; i < candidate_count; ++i) {
        if (hasPathPrefix(path, candidates[i])) {
            return true;
        }
    }
    return false;
}

static uint64_t computePathHash(const char* source_path, const char* sanitized_base) {
    uint64_t hash = FNV1A64_OFFSET;
    const char* path_for_hash = NULL;

    if (source_path && source_path[0]) {
#ifndef _WIN32
        char* resolved = realpath(source_path, NULL);
        if (resolved) {
            path_for_hash = resolved;
        } else {
            path_for_hash = source_path;
        }
#else
        char resolved[_MAX_PATH];
        if (_fullpath(resolved, source_path, _MAX_PATH)) {
            path_for_hash = resolved;
        } else {
            path_for_hash = source_path;
        }
#endif
    }

    bool treat_as_temp = isLikelyTemporaryPath(path_for_hash);
    if (!treat_as_temp && path_for_hash && path_for_hash[0]) {
        fnv1aUpdate(&hash, path_for_hash, strlen(path_for_hash));
    } else if (sanitized_base && sanitized_base[0]) {
        fnv1aUpdate(&hash, sanitized_base, strlen(sanitized_base));
    } else if (path_for_hash && path_for_hash[0]) {
        fnv1aUpdate(&hash, path_for_hash, strlen(path_for_hash));
    } else {
        fnv1aUpdate(&hash, "<none>", 6);
    }

#ifndef _WIN32
    if (path_for_hash && path_for_hash != source_path) {
        free((void*)path_for_hash);
    }
#endif

    return hash;
}

static const char* basenameForPath(const char* path) {
    if (!path) return "";
    const char* last_slash = strrchr(path, '/');
#ifdef _WIN32
    const char* last_backslash = strrchr(path, '\\');
    if (!last_slash || (last_backslash && last_backslash > last_slash)) {
        last_slash = last_backslash;
    }
#endif
    return last_slash ? last_slash + 1 : path;
}

static char* sanitizeFileComponent(const char* name) {
    if (!name) return strdup("");
    size_t len = strlen(name);
    char* sanitized = (char*)malloc(len + 1);
    if (!sanitized) return NULL;
    for (size_t i = 0; i < len; ++i) {
        unsigned char ch = (unsigned char)name[i];
        if (isalnum(ch) || ch == '.' || ch == '-' || ch == '_') {
            sanitized[i] = (char)ch;
        } else {
            sanitized[i] = '_';
        }
    }
    sanitized[len] = '\0';
    return sanitized;
}

static bool computeSourceHash(const char* source_path, bool require_file, uint64_t* out_hash) {
    if (!out_hash) return false;
    if (!source_path || source_path[0] == '\0') {
        if (require_file) return false;
        uint64_t fallback = FNV1A64_OFFSET;
        fnv1aUpdate(&fallback, "<none>", 6);
        *out_hash = fallback;
        return true;
    }
    FILE* f = fopen(source_path, "rb");
    if (!f) {
        if (!require_file) {
            uint64_t fallback = FNV1A64_OFFSET;
            fnv1aUpdate(&fallback, source_path, strlen(source_path));
            *out_hash = fallback;
            return true;
        }
        return false;
    }
    uint64_t hash = FNV1A64_OFFSET;
    unsigned char buffer[4096];
    size_t read_bytes = 0;
    while ((read_bytes = fread(buffer, 1, sizeof(buffer), f)) > 0) {
        fnv1aUpdate(&hash, buffer, read_bytes);
    }
    if (ferror(f)) {
        fclose(f);
        return false;
    }
    fclose(f);
    *out_hash = hash;
    return true;
}

/* ================= PSB3 primitives: ByteBuf (write) / Cursor (read) =====
 *
 * Every multi-byte integer in the PSB3 container goes through these helpers
 * so the on-disk format is little-endian and host-int-width independent
 * (VM 2.0 plan §5.2). Cursor reads are bounds-checked against the buffer
 * they were initialized with (never the whole file) and stick an `error`
 * flag on first overrun instead of reading out of bounds (Phase 1d loader
 * hardening); callers check `cur->error` once after a batch of reads
 * instead of per-call.
 */

typedef struct {
    uint8_t* data;
    size_t len;
    size_t cap;
} ByteBuf;

static void bufInit(ByteBuf* b) {
    b->data = NULL;
    b->len = 0;
    b->cap = 0;
}

static void bufFree(ByteBuf* b) {
    free(b->data);
    b->data = NULL;
    b->len = b->cap = 0;
}

static void bufReserve(ByteBuf* b, size_t extra) {
    if (b->len + extra <= b->cap) return;
    size_t new_cap = b->cap ? b->cap * 2 : 64;
    while (new_cap < b->len + extra) new_cap *= 2;
    uint8_t* grown = (uint8_t*)realloc(b->data, new_cap);
    if (!grown) {
        fprintf(stderr, "Memory allocation error (bytecode buffer grow failed)\n");
        EXIT_FAILURE_HANDLER();
        return;
    }
    b->data = grown;
    b->cap = new_cap;
}

static void bufBytes(ByteBuf* b, const void* p, size_t n) {
    if (n == 0) return;
    bufReserve(b, n);
    memcpy(b->data + b->len, p, n);
    b->len += n;
}

static void bufU8(ByteBuf* b, uint8_t v) { bufBytes(b, &v, 1); }

static void bufU16LE(ByteBuf* b, uint16_t v) {
    uint8_t bytes[2] = { (uint8_t)v, (uint8_t)(v >> 8) };
    bufBytes(b, bytes, 2);
}

static void bufU32LE(ByteBuf* b, uint32_t v) {
    uint8_t bytes[4] = { (uint8_t)v, (uint8_t)(v >> 8), (uint8_t)(v >> 16), (uint8_t)(v >> 24) };
    bufBytes(b, bytes, 4);
}

static void bufU64LE(ByteBuf* b, uint64_t v) {
    uint8_t bytes[8];
    for (int i = 0; i < 8; ++i) bytes[i] = (uint8_t)(v >> (8 * i));
    bufBytes(b, bytes, 8);
}

static void bufI32LE(ByteBuf* b, int32_t v) { bufU32LE(b, (uint32_t)v); }

static void bufF32LE(ByteBuf* b, float v) {
    uint32_t bits;
    memcpy(&bits, &v, sizeof(bits));
    bufU32LE(b, bits);
}

static void bufF64LE(ByteBuf* b, double v) {
    uint64_t bits;
    memcpy(&bits, &v, sizeof(bits));
    bufU64LE(b, bits);
}

/* Unsigned LEB128 varint: 7 payload bits per byte, MSB = continuation. */
static void bufVarint(ByteBuf* b, uint64_t v) {
    do {
        uint8_t byte = (uint8_t)(v & 0x7F);
        v >>= 7;
        if (v != 0) byte |= 0x80;
        bufU8(b, byte);
    } while (v != 0);
}

/* Zigzag-encoded signed varint (small magnitudes, either sign, stay small). */
static void bufSVarint(ByteBuf* b, int64_t v) {
    uint64_t zigzag = ((uint64_t)v << 1) ^ (uint64_t)(v >> 63);
    bufVarint(b, zigzag);
}

static void bufLenPrefixedBytes(ByteBuf* b, const void* p, size_t n) {
    bufVarint(b, (uint64_t)n);
    bufBytes(b, p, n);
}

typedef struct {
    const uint8_t* data;
    size_t size;
    size_t pos;
    bool error;
} Cursor;

static void curInit(Cursor* c, const uint8_t* data, size_t size) {
    c->data = data;
    c->size = size;
    c->pos = 0;
    c->error = false;
}

static bool curEnsure(Cursor* c, size_t n) {
    if (c->error) return false;
    /* Overflow-safe: avoid c->pos + n wrapping on adversarial huge n. */
    if (n > c->size - c->pos) {
        c->error = true;
        return false;
    }
    return true;
}

static uint8_t curU8(Cursor* c) {
    if (!curEnsure(c, 1)) return 0;
    return c->data[c->pos++];
}

static uint16_t curU16LE(Cursor* c) {
    if (!curEnsure(c, 2)) return 0;
    uint16_t v = (uint16_t)(c->data[c->pos] | ((uint16_t)c->data[c->pos + 1] << 8));
    c->pos += 2;
    return v;
}

static uint32_t curU32LE(Cursor* c) {
    if (!curEnsure(c, 4)) return 0;
    const uint8_t* p = c->data + c->pos;
    uint32_t v = (uint32_t)p[0] | ((uint32_t)p[1] << 8) | ((uint32_t)p[2] << 16) | ((uint32_t)p[3] << 24);
    c->pos += 4;
    return v;
}

static uint64_t curU64LE(Cursor* c) {
    if (!curEnsure(c, 8)) return 0;
    const uint8_t* p = c->data + c->pos;
    uint64_t v = 0;
    for (int i = 0; i < 8; ++i) v |= ((uint64_t)p[i]) << (8 * i);
    c->pos += 8;
    return v;
}

static int32_t curI32LE(Cursor* c) { return (int32_t)curU32LE(c); }

static float curF32LE(Cursor* c) {
    uint32_t bits = curU32LE(c);
    float v;
    memcpy(&v, &bits, sizeof(v));
    return v;
}

static double curF64LE(Cursor* c) {
    uint64_t bits = curU64LE(c);
    double v;
    memcpy(&v, &bits, sizeof(v));
    return v;
}

static bool curBytes(Cursor* c, void* out, size_t n) {
    if (!curEnsure(c, n)) return false;
    if (n > 0) memcpy(out, c->data + c->pos, n);
    c->pos += n;
    return true;
}

static uint64_t curVarint(Cursor* c) {
    uint64_t result = 0;
    int shift = 0;
    for (;;) {
        if (c->error || shift > 63) { c->error = true; return 0; }
        uint8_t byte = curU8(c);
        if (c->error) return 0;
        result |= ((uint64_t)(byte & 0x7F)) << shift;
        if ((byte & 0x80) == 0) break;
        shift += 7;
    }
    return result;
}

static int64_t curSVarint(Cursor* c) {
    uint64_t zigzag = curVarint(c);
    return (int64_t)(zigzag >> 1) ^ -(int64_t)(zigzag & 1);
}

/* Reads a varint length prefix then that many bytes; returns a malloc'd,
 * NUL-terminated buffer (length in *out_len) or NULL on error/truncation. */
static char* curLenPrefixedString(Cursor* c, size_t* out_len) {
    uint64_t len = curVarint(c);
    if (c->error || len > c->size) { c->error = true; return NULL; }
    char* s = (char*)malloc((size_t)len + 1);
    if (!s) { c->error = true; return NULL; }
    if (!curBytes(c, s, (size_t)len)) { free(s); return NULL; }
    s[len] = '\0';
    if (out_len) *out_len = (size_t)len;
    return s;
}

